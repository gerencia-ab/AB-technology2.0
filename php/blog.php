        <div class="blog-contenido">
        
        <p>
            Desde un sentido muy amplio se puede entender la WEB 3.0 como una WEB
            semántica en donde básicamente el objetivo es que cualquier usuario, vinculado
            al internet, pueda encontrar soluciones, óptimas, rápidas y efectivas a sus
            preguntas y dudas en un tiempo corto y con un lenguaje que logre satisfacer las
            necesidades del sujeto.
            </p>

            <p>
            La solución que ofrece esta WEB semántica es estructurar y organizar la
            información según su significado para una mayor comprensión y facilitando el
            entendimiento de ésta para un acceso más rápido no solo a las máquinas sino a
            cualquier persona.
            </p>

            <p>
            Con esta breve introducción es necesario clasificar ciertas características de la
            nueva WEB 3.0 :
            </p>

            <ol>
                <li>
                    <p>
                    <strong>Rapidez:</strong> Los principales operadores de telecomunicaciones han
                    implantado conexiones de banda ancha para garantizar una experiencia
                    mucho más satisfactoria y garantista por parte del usuario.
                    </p>
                </li>
                <li>
                    <p>
                    <strong>La amplitud del contenido:</strong> Los programas y las licencias para crear
                    contenido y organizaciones con el objetivo de permitir publicar obras
                    creativas son considerablemente mucho más comunes en la WEB 3.0
                    </p>
                </li>
                <li>
                    <p>
                    <strong>Computación en la nube:</strong> Con la creación de nuevos espacios de
                    almacenamiento, no sólo de datos sino de programas, la web se convierte
                    en un espacio ejecutable a modo de computador universal.
                    </p>
                </li>
                <li>
                    <p>
                    <strong>Mayor flexibilidad en la navegación:</strong> Además de la facilidad por parte del
                    usuario al momento de navegar también tiene la sencillez de crear
                    espacios que pueda modificar y personalizar de acuerdo a la expectativas
                    y gustos de la persona.
                    </p>
                </li>
            </ol>

            <p>
            Recordemos que nuestra sociedad, en el sentido más amplio, está en una
            constante evolución tecnológica, lo cual permite el incremento con celeridad de
            las redes sociales, en este sentido la WEB 3.0 nos proporciona el aumento de las
            diversas formas para conectarnos entre nosotros.
            </p>

            <p>
            Desde la parte legal es un requisito fundamental tener en cuenta que en
            Colombia estos sistemas y, en su generalidad, el INTERNET está regulado por
            la Ley No. 2108, La Ley De Internet Como Servicio Público Esencial Y
            Universal, además de la intervención del MinTIC (MINISTERIO DE
            TECNOLOGÍAS DE LA INFORMACIÓN Y LAS COMUNICACIONES) en
            las circunstancias determinadas que la propia Ley le otorga la competencia.
            </p>

            <p>
            Finalmente la WEB 3.0, aunque no hay todavía un concepto concreto de ella, es
            conveniente apreciarla, desde un punto de vista abstracto, como una evolución
            al sistema de redes que se enfoca en la mayor comprensión para el usuario
            unificando a su vez las respuestas más coherentes y favorables, cumpliendo con
            las expectativas requeridas, para abarcar todas las necesidades de la persona
            </p>

            <p>
            Para más información se puede consultar la Ley No. 2108, la página oficial del
            MinTIC <a href="https://mintic.gov.co/portal/inicio/">https://mintic.gov.co/portal/inicio/</a> Y la página de CEUPE
            <a href="https://www.ceupe.com/blog/que-es-la-web-3-0.html">https://www.ceupe.com/blog/que-es-la-web-3-0.html</a>
            </p>
