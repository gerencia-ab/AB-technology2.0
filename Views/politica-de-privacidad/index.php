                    <h3>Definiciones</h3>
                </div>
                <ul style="list-style-type: none;">
                    <li><b>Autorización:</b> Consentimiento previo, expreso e informado del Titular para llevar a cabo el
                    Tratamiento de datos personales.</li>
                    <li><b>Base de Datos:</b> Conjunto organizado de datos personales que sea objeto de Tratamiento.</li>
                    <li><b>Dato personal:</b> Cualquier información vinculada o que pueda asociarse a una o varias
                    personas naturales determinadas o determinables.</li>
                    <li><b>Encargado:</b> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con
                    otras, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento.</li>
                    <li><b>Responsable:</b> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con
                    otras, decida sobre la base de datos y/o el Tratamiento de los datos.</li>
                    <li><b>Titular:</b> Persona natural cuyos datos personales sean objeto de Tratamiento;</li>
                    <li><b>Tratamiento:</b> Cualquier operación o conjunto de operaciones sobre datos personales, tales
                    como la recolección, almacenamiento, uso, circulación o supresión.</li>
                    <li><b>Principio de legalidad:</b> El Tratamiento de datos personales es una actividad reglada que
                    debe sujetarse a lo establecido en la ley y en las demás disposiciones que la desarrollen;</li>
                    <li><b>Principio de finalidad:</b> El Tratamiento debe obedecer a una finalidad legítima de acuerdo
                    con la Constitución y la Ley, la cual debe ser informada al Titular.</li>
                    <li><b>Principio de libertad:</b> El Tratamiento sólo puede ejercerse con el consentimiento, previo,
                    expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados
                    sin previa autorización, o en ausencia de mandato legal o judicial que releve el
                    consentimiento.</li>
                    <li><b>Principio de veracidad o calidad:</b> La información sujeta a Tratamiento debe ser veraz,
                    completa, exacta, actualizada, comprobable y comprensible. Se prohíbe el Tratamiento de
                    datos parciales, incompletos, fraccionados o que induzcan a error.</li>
                    <li><b>Principio de transparencia:</b> En el Tratamiento debe garantizarse el derecho del Titular a
                    obtener del Responsable del Tratamiento o del Encargado del Tratamiento, en cualquier
                    momento y sin restricciones, información acerca de la existencia de datos que le concierne.</li>
                    <li><b>Principio de acceso y circulación restringida:</b> El Tratamiento se sujeta a los límites que se
                    derivan de la naturaleza de los datos personales, de las disposiciones de la ley y la
                    Constitución. En este sentido, el Tratamiento sólo podrá hacerse por personas autorizadas por
                    el Titular y/o por las personas previstas en la ley. <br> Los datos personales, salvo la información pública, no podrán estar disponibles en Internet u
                    otros medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente
                    controlable para brindar un conocimiento restringido sólo a los Titulares o terceros
                    autorizados conforme a la ley.</li>
                    <li><b>Principio de seguridad:</b> La información sujeta a Tratamiento por el Responsable del
                    Tratamiento o Encargado del Tratamiento se deberá manejar con las medidas técnicas,
                    humanas y administrativas que sean necesarias para otorgar seguridad a los registros evitando
                    su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.</li>
                    <li><b>Principio de confidencialidad:</b> Todas las personas que intervengan en el Tratamiento de
                    datos personales, que no tengan la naturaleza de públicos, están obligadas a garantizar la
                    reserva de la información, inclusive después de finalizada su relación con alguna de las
                    labores que comprende el Tratamiento, pudiendo sólo realizar suministro o comunicación de
                    datos personales cuando ello corresponda al desarrollo de las actividades autorizadas en la ley
                    y en los términos de la misma.</li>
